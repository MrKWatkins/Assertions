namespace MrKWatkins.Assertions;

/// <summary>
/// Specifies the display format for integer values in assertion messages.
/// </summary>
public enum IntegerFormat
{
    /// <summary>
    /// Display integers in decimal format.
    /// </summary>
    Decimal,

    /// <summary>
    /// Display integers in hexadecimal format.
    /// </summary>
    Hexadecimal,

    /// <summary>
    /// Display integers in binary format.
    /// </summary>
    Binary
}